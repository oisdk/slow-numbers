data Digit
  = Zero
  | One
  | Two
  | Three
  | Four
  | Five
  | Six
  | Seven
  | Eight
  | Nine
  deriving (Eq, Ord, Bounded, Enum, Generic, Data, Typeable)

instance NFData Digit where
    rnf x = x `seq` ()

instance Show Digit where
    showsPrec _ = shows . fromEnum
    showList ns s = foldr ((:) . f) s ns where
      f Zero  = '0'
      f One   = '1'
      f Two   = '2'
      f Three = '3'
      f Four  = '4'
      f Five  = '5'
      f Six   = '6'
      f Seven = '7'
      f Eight = '8'
      f Nine  = '9'

instance Read Digit where
    readsPrec _ ys = lex ys >>= \case
      ("0",xs) -> [(Zero,xs)]
      ("1",xs) -> [(One,xs)]
      ("2",xs) -> [(Two,xs)]
      ("3",xs) -> [(Three,xs)]
      ("4",xs) -> [(Four,xs)]
      ("5",xs) -> [(Five,xs)]
      ("6",xs) -> [(Six,xs)]
      ("7",xs) -> [(Seven,xs)]
      ("8",xs) -> [(Eight,xs)]
      ("9",xs) -> [(Nine,xs)]
      _ -> []

newtype Nat = Nat { getNat :: [Digit] } deriving (Eq, Generic, Data, Typeable)

instance Bounded Nat where
    minBound = Nat []
    maxBound = Nat (repeat Nine)

instance Enum Nat where
    toEnum = fromIntegral
    fromEnum (Nat xs) = foldr f 0 xs where
      f e a = fromEnum e + a * 10
    succ = (+) 1

instance NFData Nat where
    rnf (Nat xs) = rnf xs

instance Show Nat where
    showsPrec _ (Nat ns) s = foldl' (flip ((:) . f)) s ns where
      f Zero  = '0'
      f One   = '1'
      f Two   = '2'
      f Three = '3'
      f Four  = '4'
      f Five  = '5'
      f Six   = '6'
      f Seven = '7'
      f Eight = '8'
      f Nine  = '9'

instance Read Nat where
    readsPrec d ys =
        [ ((fromIntegral :: Natural -> Nat) x, xs)
        | (x,xs) <- readsPrec d ys ]

instance Ord Nat where
    compare (Nat n) (Nat m) = go n m where
      go [] []             = EQ
      go (_ : _) []        = GT
      go [] (_ : _)        = LT
      go (x : xs) (y : ys) = go xs ys `mappend` compare x y

instance Num Nat where
    fromInteger = Nat . go . abs
      where
        go 0 = []
        go n =
            case quotRem n 10 of
                (q,0) -> Zero : go q
                (q,1) -> One : go q
                (q,2) -> Two : go q
                (q,3) -> Three : go q
                (q,4) -> Four : go q
                (q,5) -> Five : go q
                (q,6) -> Six : go q
                (q,7) -> Seven : go q
                (q,8) -> Eight : go q
                (q,9) -> Nine : go q
                _     -> undefined
    Nat n + Nat m = Nat (go Zero n m)
      where
        go c xs []         = carry c xs
        go c [] ys         = carry c ys
        go c (x:xs) (y:ys) = sumDig c x y : go (carryDig c x y) xs ys
        carry Zero xs  = xs
        carry c []     = [c]
        carry c (x:xs) = sumBin c x : carry (carrySumBin c x) xs
    Nat n * Nat m = go n m
      where
        go (x:xs) ys =
            Nat (mulEach Zero x ys) +
            (\(Nat nn) ->
                  Nat (Zero : nn))
                (go xs ys)
        go _ _ = Nat []
        mulEach Zero _ []  = []
        mulEach c _ []     = [c]
        mulEach c x (y:ys) = digMul c x y : mulEach (carryDigMul c x y) x ys
    Nat n - Nat m = Nat (go Zero n m) where
        go c (x:xs) (y:ys) = sub c x y : go (subCarry c x y) xs ys
        go c xs []         = carry c xs
        go c [] ys         = getNat (Nat [c] + Nat ys)
        carry Zero xs  = xs
        carry c (x:xs) = subBin c x : carry (subBinCarry c x) xs
        carry c []     = [c]
    abs = id
    signum (Nat []) = Nat []
    signum _        = Nat [One]

